package org.essoft

fun main() {
    val x = 5

    if (x > 0) {
        println("x is positive")
    } else if (x < 0) {
        println("x is negative")
    } else {
        println("x is zero")
    }

    if (x % 2 == 0) {
        println("x is even")
    } else {
        println("x is odd")
    }

}